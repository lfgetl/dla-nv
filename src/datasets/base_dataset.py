import logging
import random
from typing import List

import torch
import torchaudio
from torch.utils.data import Dataset

from src.model.mel_spectrogram import MelSpectrogram, MelSpectrogramConfig

logger = logging.getLogger(__name__)


class BaseDataset(Dataset):
    """
    Base class for the datasets.

    Given a proper index (list[dict]), allows to process different datasets
    for the same task in the identical manner. Therefore, to work with
    several datasets, the user only have to define index in a nested class.
    """

    def __init__(
        self,
        index,
        is_val=False,
        limit=None,
        shuffle_index=False,
        instance_transforms=None,
    ):
        """
        Args:
            index (list[dict]): list, containing dict for each element of
                the dataset. The dict has required metadata information,
                such as label and object path.
            limit (int | None): if not None, limit the total number of elements
                in the dataset to 'limit' elements.
            shuffle_index (bool): if True, shuffle the index. Uses python
                random package with seed 42.
            instance_transforms (dict[Callable] | None): transforms that
                should be applied on the instance. Depend on the
                tensor name.
        """
        self._assert_index_is_valid(index)

        index = self._shuffle_and_limit_index(index, limit, shuffle_index)
        self._index: List[dict] = index
        self.mel_spectrogram = MelSpectrogram(MelSpectrogramConfig())
        self.target_sr = MelSpectrogramConfig().sr
        self.len_segment = 11025
        self.instance_transforms = instance_transforms
        self.is_val = is_val

    def __getitem__(self, ind):
        """
        Get element from the index, preprocess it, and combine it
        into a dict.

        Notice that the choice of key names is defined by the template user.
        However, they should be consistent across dataset getitem, collate_fn,
        loss_function forward method, and model forward method.

        Args:
            ind (int): index in the self.index list.
        Returns:
            instance_data (dict): dict, containing instance
                (a single dataset element).
        """
        file_id = self._index[ind]["file_id"]
        audio_path = self._index[ind]["audio_path"]
        spectrogram = None
        if audio_path != "":
            target_audio = self.load_audio(audio_path)
        else:
            target_audio = None
            spectrogram = self._index[ind]["spectrogram"]

        instance_data = {
            "file_id": file_id,
        }
        if spectrogram is not None:
            instance_data["spectrogram"] = spectrogram
        if target_audio is not None:
            instance_data["target_audio"] = target_audio
        instance_data = self.preprocess_data(instance_data)

        return instance_data

    def __len__(self):
        """
        Get length of the dataset (length of the index).
        """
        return len(self._index)

    def load_audio(self, path):
        audio_tensor, sr = torchaudio.load(path)
        audio_tensor = audio_tensor[0:1, :]  # remove all channels but the first
        target_sr = self.target_sr
        if sr != target_sr:
            audio_tensor = torchaudio.functional.resample(audio_tensor, sr, target_sr)
        if not self.is_val:
            T = audio_tensor.shape[-1]
            if T < self.len_segment:
                return audio_tensor
            right = T - self.len_segment
            start = random.randint(0, right)
            return audio_tensor[..., start : start + self.len_segment]

        return audio_tensor

    def get_spectrogram(self, audio):
        """
        Special instance transform with a special key to
        get spectrogram from audio.

        Args:
            audio (Tensor): original audio.
        Returns:
            spectrogram (Tensor): complex spectrogram for the audio.
        """
        return self.mel_spectrogram(audio)

    def preprocess_data(self, instance_data):
        """
        Preprocess data with instance transforms.

        Each tensor in a dict undergoes its own transform defined by the key.

        Args:
            instance_data (dict): dict, containing instance
                (a single dataset element).
        Returns:
            instance_data (dict): dict, containing instance
                (a single dataset element) (possibly transformed via
                instance transform).
        """
        if self.instance_transforms is not None:
            for transform_name in self.instance_transforms.keys():
                if transform_name == "get_spectrogram":
                    if "spectrogram" not in instance_data:
                        instance_data["spectrogram"] = self.get_spectrogram(
                            instance_data["target_audio"]
                        )
                if transform_name == "audio":
                    instance_data["target_audio"] = self.instance_transforms["audio"](
                        instance_data["target_audio"]
                    )
        return instance_data

